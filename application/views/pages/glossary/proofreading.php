<h1><a href="/glossary.php">Glossary</a> - Proofreading</h1>
<p>is an act, a process where a proofreader will check a document for errors and typos and remove them before the final version is printed, sent, published, or submitted. The definition of proofreader presents any human who looks for human error in writing with the goal of eliminating that error and creating an accurate, proof read content.</p>
<p>This act requires a set of techniques that will detect and mark the errors the writer has unintentionally done while writing. When the writer has an idea and decides to put it on paper, it is the task of a proofreader or the writer himself to get rid of any punctuation errors and other typos in the writing. For this purpose, the proofreader must carefully re-read all the content and understand the context to see what it is supposed to mean and whether or not it is accurately presented.</p>
